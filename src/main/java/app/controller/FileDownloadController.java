package app.controller;

import app.filegeneration.FileTextGenerator;
import groovy.transform.AutoClone;
import java.io.File;
import java.io.FileInputStream;
import java.io.FileWriter;
import java.io.IOException;
import java.io.OutputStream;
import java.io.PrintWriter;
import javax.servlet.ServletContext;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RequestParam;

/**
 *
 * @author Sara ja Laur
 */
@Controller
@RequestMapping("/bibtexinator/download")
public class FileDownloadController {

    /**
     * Size of a byte buffer to read/write file
     */
    private static final int BUFFER_SIZE = 4096;

    /**
     * Path of the file to be downloaded, relative to application's directory
     */
    private String filePath;

    /**
     * Method for handling file download request from client
     */
    
    @Autowired
    private FileTextGenerator ftg;
    
    @RequestMapping(method = RequestMethod.GET)
    public void doDownload(HttpServletRequest request, HttpServletResponse response,
            @RequestParam(required = false, defaultValue = "sigproc") String fileName) throws IOException {
        
        
        // get absolute path of the application
        ServletContext context = request.getServletContext();
        String appPath = context.getRealPath("");
        System.out.println("appPath = " + appPath);

        // construct the complete absolute path of the file
        filePath = fileName + ".bib";
        String fullPath = appPath + filePath;
        File downloadFile = new File(fullPath);
        downloadFile.createNewFile();

        //write generated text
        
        PrintWriter pw = null;
        FileWriter fw = new FileWriter(downloadFile, true);
        pw = new PrintWriter(fw);
        System.out.println(ftg.generateBibtexFromEntrys());
        pw.println(ftg.generateBibtexFromEntrys());
        pw.close();

        FileInputStream inputStream = new FileInputStream(downloadFile);

        // get MIME type of the file
        String mimeType = context.getMimeType(fullPath);
        if (mimeType == null) {
            // set to binary type if MIME mapping not found
            mimeType = "application/octet-stream";
        }
        System.out.println("MIME type: " + mimeType);

        // set content attributes for the response
        response.setContentType(mimeType);
        response.setContentLength((int) downloadFile.length());

        // set headers for the response
        String headerKey = "Content-Disposition";
        String headerValue = String.format("attachment; filename=\"%s\"",
                downloadFile.getName());
        response.setHeader(headerKey, headerValue);

        // get output stream of the response
        OutputStream outStream = response.getOutputStream();

        byte[] buffer = new byte[BUFFER_SIZE];
        int bytesRead = -1;

        // write bytes read from the input stream into the output stream
        while ((bytesRead = inputStream.read(buffer)) != -1) {
            outStream.write(buffer, 0, bytesRead);
        }

        inputStream.close();
        outStream.close();

    }
}
